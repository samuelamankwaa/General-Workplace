/****** Script for SelectTopNRows command from SSMS  ******/
SELECT  A.[DL_LOAD_TS]
      ,A.[DL_LOAD_DATE]
      ,[LOANNUMBER]
      ,[MEMBERNUMBER]
      ,[DOB]
      ,[XPLOANID]
      ,[TIER]
      ,[CREDITSCORE]
      ,[CUSTOMSCORE]
      ,[BANKRUPTCYSCORE]
      ,[OCCUPANCYSTATUS]
      ,[OCCUPANCYDURATION]
      ,[EMPLOYMENTSTATUS]
      ,[EMPLOYEDMONTHS]
      ,[AMOUNTAPPROVED]
      ,[AMOUNTREQUESTED]
      ,[LOANTERM]
      ,[MONTHLYINCOMEBASESALARY]
      ,[TOTALDEBTTOINCOMEFUND]
      ,[TOTALDEBTTOINCOMEUW]
      ,[PAYMENTTOINCOME]
      ,[TOTALUNSECUREBALANCEOVERANNUALINCOME]
      ,[TOTALSECUREBALANCEEXCLUDEMTGOVERANNUALINCOME]
      ,[AVAILABLEREVOLVEAMTOVERTOTALREVCREDIT]
      ,[APPROVEDLTV]
      ,[FUNDEDLTV]
      ,[APPROVALDATEFIRST]
      ,[APPROVALDATE]
      ,[DECIDINGDATE]
      ,[LOANSTATUS]
      ,[LOANTYPE]
      ,[PURPOSETYPE]
  FROM ID.MonthEnd_DL_Load_Dates d 
  JOIN [DataMart].[dbo].[ML_Daily_Report] A
  ON D.dl_load_Date = A.DL_LOAD_DATE
  WHERE D.sequence = 1
  ORDER BY [DECIDINGDATE]